def featured(number)
  number += 1
  loop do
    break if number.odd? && number % 7 == 0 && number.to_s.chars == number.to_s.chars.uniq
    break if number > 9876543210
    number += 1
  end
  
  loop do
    break if number.to_s.chars == number.to_s.chars.uniq
    break if number > 9876543210 
    number += 14
  end
    
  if number > 9876543210
    "there is no next featured number"
  else
    number
  end
end

