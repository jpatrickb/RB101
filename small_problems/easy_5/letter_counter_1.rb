def word_sizes(string)
  words = string.split(' ')
  amount_sizes = {}
  words.each do |word|
    if amount_sizes[word.size]
      amount_sizes[word.size] += 1
    else 
      amount_sizes[word.size] = 1  
    end
  end
amount_sizes
end

