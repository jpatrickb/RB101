def letter_case_count(string)
  hash = {}
  chars = string.chars
  hash[:lowercase] = chars.count {|char| char =~ /[a-z]/}
  hash[:uppercase] = chars.count {|char| char =~ /[A-Z]/}
  hash[:neither] = chars.count {|char| char =~ /[^a-zA-Z]/}
  hash
end


